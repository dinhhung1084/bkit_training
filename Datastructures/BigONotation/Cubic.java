package Datastructures.BigONotation;

public class Cubic {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 6; j++) {
                for (int k = 0; k < 6; k++) {
                    System.out.println(i +" "+ j+" "+k);
                }
            }
        }
    }
}
